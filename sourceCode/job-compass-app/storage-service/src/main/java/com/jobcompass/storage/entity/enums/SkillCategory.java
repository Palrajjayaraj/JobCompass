package com.jobcompass.storage.entity.enums;

/**
 * Enumeration of skill categories.
 * Categorizes different types of technical skills.
 * 
 * @author Palrajjayaraj
 */
public enum SkillCategory {
    /**
     * Programming languages (e.g., Java, Python, JavaScript)
     */
    PROGRAMMING_LANGUAGE,
    
    /**
     * Software frameworks (e.g., Spring Boot, React, Django)
     */
    FRAMEWORK,
    
    /**
     * Database technologies (e.g., PostgreSQL, MongoDB, Redis)
     */
    DATABASE,
    
    /**
     * Cloud platforms (e.g., AWS, Azure, GCP)
     */
    CLOUD,
    
    /**
     * DevOps tools (e.g., Docker, Kubernetes, Jenkins)
     */
    DEVOPS,
    
    /**
     * Software tools (e.g., Git, JIRA, Postman)
     */
    TOOL,
    
    /**
     * Soft skills (e.g., Leadership, Communication)
     */
    SOFT_SKILL,
    
    /**
     * Other/uncategorized skills
     */
    OTHER
}
